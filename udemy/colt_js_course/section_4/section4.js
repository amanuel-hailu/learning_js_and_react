// Javascript double equals vs triple equals
// == double equals checks for value only
// it does not check for type
4 == "4"; // true
// === triple equals checks for value and type
// it checks for type
4 === "4"; // false

// For non equality there are two options: != and !==
// The operator != checks for value only
1 != "1"; // false
// operator !== checks for value and type
1 !== "1"; // true
