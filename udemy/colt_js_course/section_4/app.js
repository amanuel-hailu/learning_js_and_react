// Conditionals

// If / Else Examples
// Check if a number is even or odd
const num = 5;
if (num % 2 === 0) {
  console.log("Even");
} else {
  console.log("Odd");
}

// If/else example
const rating = 3;
if (rating === 3) {
  console.log("You are a superstar!");
} else if (rating === 2) {
  console.log("MEH");
}
